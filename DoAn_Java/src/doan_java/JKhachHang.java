    public khachhang getModel() {
        khachhang ee = new khachhang();
        ee.setId(Integer.parseInt(txtid.getText()));
        ee.setMaKH(txtmaKH.getText());
        ee.setTenKH(txttenKH.getText());
        ee.setdC(txtdc.getText());
        ee.setSdt(txtsdt.getText());
        return ee;
    }
      public boolean validateForm() {
        if (txtid.getText().isEmpty() || txtmaKH.getText().isEmpty()) {
            return false;
        }
        return true;
    }
    private void btnluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnluuActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            khachhang ee = getModel();
            try {
                if (dao.add(ee) > 0) {
                    JOptionPane.showMessageDialog(this, "Lưu thành công!!!");
                    filldataTable();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Loi:" + e.toString());
            }
        } else {
            JOptionPane.showMessageDialog(this, "bạn chưa nhập đầy đủ thông tin!!!");
        }
    }//GEN-LAST:event_btnluuActionPerformed

    private void tblFormKHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFormKHMouseClicked
        // TODO add your handling code here:
        int id = tblFormKH.rowAtPoint(evt.getPoint());
        id_KH = tblFormKH.getValueAt(id, 0).toString();
        khachhang ee = dao.getkhachhangByID(Integer.parseInt(id_KH));
        setModel(ee);
    }//GEN-LAST:event_tblFormKHMouseClicked

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        // TODO add your handling code here:
         if (validateForm()) {
            try {
                khachhang eeNew = getModel();
                if (dao.edit(eeNew) > 0) {
                    JOptionPane.showMessageDialog(this, "Sửa thành công!!!!");
                    filldataTable();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Loi:" + e.toString());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập đầy đủ thông tin! ","Thông báo",1);
        }
    }//GEN-LAST:event_btnsuaActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        // TODO add your handling code here:
           if (txtid.getText().isEmpty() || id_KH.length() == 0) {
            JOptionPane.showMessageDialog(null, "bạn chưa nhập ID để xóa!!!","Thông báo",1);
        } else {
            try {
                if (id_KH.length() > 0) {
                    dao.del(Integer.parseInt(id_KH));
                } else {
                    dao.del(Integer.parseInt(txtid.getText()));
                }
                filldataTable();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "loi:" + e.toString());
            }

        }
    }//GEN-LAST:event_btnxoaActionPerformed

    private void txttimkiemKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txttimkiemKeyReleased
        // TODO add your handling code here:
           ArrayList<khachhang> al = null;
        al = new ArrayList<khachhang>();
        String val = txttimkiem.getText().toString();
        try {
            Connection conn = getDBConnect();
            String qry = "select * from tblKhachHang where MaKH like '%" + val + "%'";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(qry);
            khachhang kh;
            while (rs.next()) {
                kh = new khachhang(rs.getInt(1), rs.getString("MaKH"),
                         rs.getString(3), rs.getString(4), rs.getString(5));
                al.add(kh);
            }

            DefaultTableModel model = (DefaultTableModel) tblFormKH.getModel();
            model.setRowCount(0); // Empty/clear the table
            Object[] row = new Object[5];
            for (int i = 0; i < al.size(); i++) {
                row[0] = al.get(i).getId();
                row[1] = al.get(i).getMaKH();
                row[2] = al.get(i).getTenKH();
                row[3] = al.get(i).getdC();
                row[4] = al.get(i).getSdt();
                model.addRow(row);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_txttimkiemKeyReleased

