    public void filldataTable() {
        DefaultTableModel model = (DefaultTableModel) tblFormKH.getModel();
        model.setRowCount(0);
        for (khachhang ee : dao.getAllkhachhang()) {
            Object dataRow[] = new Object[5];
            dataRow[0] = ee.getId();
            dataRow[1] = ee.getMaKH();
            dataRow[2] = ee.getTenKH();
            dataRow[3] = ee.getdC();
            dataRow[4] = ee.getSdt();
            model.addRow(dataRow);
        }
    }

